    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
       // TODO add your handling code here:
       String notes;
       String healthIssues;
       String prescription;
       String Allergies;
       Disease disease;
       Encounter encounter=new Encounter();
       if(yesRadioButton.isSelected()){
          notes=notesTextArea.getText().replaceAll("(?!\\r)\\n", "\r\n");
          healthIssues=healthTextArea.getText().replaceAll("(?!\\r)\\n", "\r\n");
          prescription=prescriptionTextArea.getText().replaceAll("(?!\\r)\\n", "\r\n");
          Allergies=allergiesTextArea.getText().replaceAll("(?!\\r)\\n", "\r\n");
          disease=(Disease)diseaseComboBox.getSelectedItem();
          if(notes.trim().equals("")||healthIssues.trim().equals("")||prescription.trim().equals("")||Allergies.trim().equals("")){
              JOptionPane.showMessageDialog(null,"Please enter texts in all TextArea");
              return;
          }
          cwr.getApp().setAppAvailStatus("Completed");
          encounter.setAllergies(Allergies);
          encounter.setDiagnosisNotes(healthIssues);
          encounter.setDisease(disease);
          encounter.setHealthIssues(healthIssues);
          encounter.setPrescription(prescription);
          cwr.getApp().setEncounter(encounter);
          cwr.getApp().setReportStatus("Visit Detail Updated");
         
          cwr.setStatus("COMPLETED");
          cwr.setReceiver(userAccount.getPerson());
          cwr.setMessage("Patient Visit details updated");
          
          
          
          
       }else{
           notes="NA";
           healthIssues="NA";
           prescription="NA";
           Allergies="NA";
           disease=null;
          cwr.getApp().setAppAvailStatus("NoVisit");
          encounter.setAllergies(Allergies);
          encounter.setDiagnosisNotes(healthIssues);
          encounter.setDisease(disease);
          encounter.setHealthIssues(healthIssues);
          encounter.setPrescription(prescription);
          cwr.getApp().setEncounter(encounter);
          cwr.getApp().setReportStatus("NoVisit");
         
          cwr.setStatus("COMPLETED");
          cwr.setReceiver(userAccount.getPerson());
          cwr.setMessage("Customer not Visited");
           
       }
                JOptionPane.showMessageDialog(null,"WorkRequest Completed successfully");
                
                Component[] componentArray = sequencer.getComponents();
                
                Component component = componentArray[componentArray.length - 2];
                TPOperationWorkAreaPanel panel = (TPOperationWorkAreaPanel) component;
                panel.populateOpenWRTable("visit");
                panel.populateclosedWRTable();
                CardLayout layout=(CardLayout)sequencer.getLayout();
                
                
                               
               
       
       
    }//GEN-LAST:event_submitButtonActionPerformed

