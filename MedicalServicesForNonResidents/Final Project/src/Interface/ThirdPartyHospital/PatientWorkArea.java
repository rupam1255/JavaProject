    private void display() {
        Consumer c=(Consumer)cwr.getSender();
        pidLabel.setText(c.getAccountID());
        pnameLabel.setText(c.getName());
        dateLabel.setText(sdf.format(cwr.getApp().getStartDt()));
        dnameLabel.setText(cwr.getApp().getDoctor().getName());
        deptNameLabel.setText(cwr.getApp().getDoctor().getDept().getName());
        populateDisease();
        populateTests();
        
        
            notesTextArea.setEnabled(false);
            diseaseComboBox.setEnabled(false);
            healthTextArea.setEnabled(false);
            prescriptionTextArea.setEnabled(false);
            allergiesTextArea.setEnabled(false);
            testTable.setEnabled(false);
            testComboBox.setEnabled(false);
            saveTestButton.setEnabled(false);
    }

    private void populateDisease() {
        diseaseComboBox.removeAllItems();
        for(Disease d:system.getDiseaseCatalog()){
            diseaseComboBox.addItem(d);
        }
    }

    private void populateTests() {
        testComboBox.removeAllItems();
        for(Test t:enterprise.getTestCatalog()){
            testComboBox.addItem(t);
        }
    }
