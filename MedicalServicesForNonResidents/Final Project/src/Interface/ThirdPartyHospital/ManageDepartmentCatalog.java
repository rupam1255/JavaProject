    private void btnRemoveDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveDeptActionPerformed
        // TODO add your handling code here:
        int row=tableDepartment.getSelectedRow();
        Department dept=(Department)tableDepartment.getValueAt(row,0);
        enterprise.getDeptCatalog().getDepatmentList().remove(dept);
        JOptionPane.showMessageDialog(null,"Department removed successfully");
        populateDepartmentTable();
    }//GEN-LAST:event_btnRemoveDeptActionPerformed


