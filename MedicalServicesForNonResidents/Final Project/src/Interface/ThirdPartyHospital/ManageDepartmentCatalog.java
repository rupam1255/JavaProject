    public void populateDepartmentTable(){
        DefaultTableModel model = (DefaultTableModel) tableDepartment.getModel();
        model.setRowCount(0);
        
        for (Department dept : enterprise.getDeptCatalog().getDepatmentList()){
            
            Object[] row = new Object[1];
            row[0] = dept;
            model.addRow(row);
            
        }
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String departmentName = textDepartmentName.getText();
        for(Department dept:enterprise.getDeptCatalog().getDepatmentList()){
            if(dept.getName().equalsIgnoreCase(departmentName)){
                JOptionPane.showMessageDialog(null, "This Department already exist!","ErrorMessage",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        CareType type=(CareType)typeComboBox.getSelectedItem();
        Department d = enterprise.getDeptCatalog().createDepartment(departmentName,type);
        //Department d = enterprise.getDeptcatCatalog().createDepartment(departmentName);
        JOptionPane.showMessageDialog(null, "Department Catalog");
        populateDepartmentTable();
    }//GEN-LAST:event_btnSaveActionPerformed

