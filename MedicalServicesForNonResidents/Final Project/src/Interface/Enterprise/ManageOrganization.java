    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String orgName = textOrganizationName.getText();
        OrganizationType orgType = (OrganizationType) comboOrganizationType.getSelectedItem();
        
        if(orgName.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Enter some valid name!");
            return;
        }
        for(Organization org:enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org.getType()==orgType && orgType!=Organization.OrganizationType.EmbassyOrganization){
               JOptionPane.showMessageDialog(null,orgType.getValue()+ " type of Organization already exist in "+enterprise.getName()+ " enterprise", "ErrorMessage",JOptionPane.ERROR_MESSAGE);
               return; 
            }
        }
        //String networkName = networkNameJTextField.getText();
            Organization organization = enterprise.getOrganizationDirectory().createAndAddOrganization(orgType, orgName, enterprise, network, system);
            if(organization!=null){
            JOptionPane.showMessageDialog(null, "Organization added!");
            }else{
                JOptionPane.showMessageDialog(null, "Organization already exist");
            }
            
            populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnRemoveOrganizationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveOrganizationActionPerformed
        // TODO add your handling code here:
        int selectedRow = tableOrganization.getSelectedRow();
      if(selectedRow >= 0)
        {
         Organization o = (Organization)tableOrganization.getValueAt(selectedRow, 0);
         enterprise.getOrganizationDirectory().removeOrganization(String.valueOf(o));
         JOptionPane.showMessageDialog(null,"Organization Deleated Successfully");
        }else
            JOptionPane.showMessageDialog(null, "Please Select An User");
      populateTable();
        
    }//GEN-LAST:event_btnRemoveOrganizationActionPerformed

