    public void populateorganizationTypeCombo(){
        
        for (Organization.OrganizationType type : Organization.OrganizationType.values()) {

           if (!type.getValue().equals(userAccount.getRole())){//Type.Admin.getValue())) {

            if (enterprise instanceof HospitalEnterprise) {
                if (type.getValue().equals(type.TPHospitalAppintmentScheduler.getValue())) {
                        comboOrganizationType.addItem(type);

                } else if (type.getValue().equals(type.TPHospitalOperation.getValue())) {
                        comboOrganizationType.addItem(type);
            }}
            if (enterprise instanceof GovernmentEnterprise) {
                if (type.getValue().equals(type.GovernmentOrganization.getValue())) {
                        comboOrganizationType.addItem(type);

                   }else if (type.getValue().equals(type.EmbassyOrganization.getValue())) {
                        comboOrganizationType.addItem(type);
            }}
            if (enterprise instanceof ConsumerEnterprise) {
                if (type.getValue().equals(type.ConsumerOrganization.getValue())) {
                        comboOrganizationType.addItem(type);
                   }}

}
}
        
    }
    
    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tableOrganization.getModel();
        model.setRowCount(0);
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization;
            row[1] = organization.getType().getValue();
            
            model.addRow(row);
        }
    }

