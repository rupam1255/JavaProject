    private void btnRemovePersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemovePersonActionPerformed
        // TODO add your handling code here:
                int selectedRow = tablePerson.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select a row to be deleted!");
            return;
        }

        Organization organization = (Organization) tablePerson.getValueAt(selectedRow, 0);
        UserAccount userAccount = (UserAccount) tablePerson.getValueAt(selectedRow, 2);

        organization.getUserAccountDirectory().removeUserAccount(userAccount);
        Person person = userAccount.getPerson();
        organization.getPersonDirectory().removePerson(person);
        populatePersonTable();
        JOptionPane.showMessageDialog(null, "User Account removed!");
    }//GEN-LAST:event_btnRemovePersonActionPerformed

