    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        String firstName = textFirstName.getText();
        String lastName = textLastName.getText();
        String name = firstName+lastName;
        String userName = textPersonUsername.getText();
        String password = String.valueOf(textPersonConfirmPassowrd.getPassword());
        String confirmPassword = String.valueOf(textPersonConfirmPassowrd.getPassword());

        if (firstName.trim().equals("") || lastName.trim().equals("")
                || userName.trim().equals("")
                || password.trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Kindly fill proper inputs!");
            return;
        }

        if (!password.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(null, "Password does not match!");
            return;
        }

        for (Network network : system.getNetworkDirectory()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    Boolean isUserNameUsed = enterprise.getUserAccountDirectory().checkUserAccountAvailability(userName);
                    if (isUserNameUsed == true) {
                        JOptionPane.showMessageDialog(null, "User Name Already in use!");
                        return;
                    }
                }
            }
        }

        //Organization organization = (Organization) comboOrganization.getSelectedItem();
//        int userAccountListSize = organization.getUserAccountDirectory().getUserAccountList().size();
//        if (userAccountListSize > 0) {
//            JOptionPane.showMessageDialog(null, "User Account already exist for the selected organization. Only one account can "
//                + "be created for an enterprise");
//            return;
//        }
        //if (organization != null) {
        Organization organization = (Organization) comboOrg.getSelectedItem();
        Person person = organization.getPersonDirectory().addAndCreatePerson(name);
        
        for (Role role : organization.getSupportedRole()) {
            UserAccount userAccount = organization.getUserAccountDirectory().createAndAddUserAccount(userName, password, role, person);
            userAccount.setPerson(person);
        }
        populatePersonTable();
        reset();
        JOptionPane.showMessageDialog(null, "User Account created!");
        //}
        
    }//GEN-LAST:event_btnSaveActionPerformed

